#include <stdio.h>
#include <stdlib.h>
#include <tchar.h>
#include <windows.h>
#include "log_utils.h"

void log_info(const char* str, ...)
{
    if (str)
    {
        va_list args;
        va_start(args, str);

        vfprintf(stdout, str, args);

        va_end(args);
    }
}

void log_error(const char* str, ...)
{
    if (str)
    {
        va_list args;
        va_start(args, str);

        vfprintf(stderr, str, args);

        va_end(args);
    }
}